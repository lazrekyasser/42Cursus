#include "ft_printf.h"

void	write_pointer(unsigned long int n)
{
	if (n >= 16)
		write_pointer(n / 16);
	n = n % 16;
	n += n < 10 ? '0' : 'a' - 10;
	write(1, &n, 1);
}
