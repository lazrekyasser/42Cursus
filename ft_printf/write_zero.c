#include "ft_printf.h"

int	write_zero(int n)
{
	int	i;

	i = 0;
	if (n <= 0)
		return (0);
	else
	{
		while (n > 0)
		{
			write(1, "0", 1);
			n--;
			i++;
		}
	}
	return (i);
}
